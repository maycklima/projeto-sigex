package model;

/**
 *
 * @author mayck
 */
public class Participante 
{
    private String nomeParticipante;
    private String sexoParticipante;
    private String emailParticipante;
    private String tipoParticipante;
    private String alteracaoParticipante;

    public String getNomeParticipante() {
        return nomeParticipante;
    }

    public void setNomeParticipante(String nomeParticipante) {
        this.nomeParticipante = nomeParticipante;
    }

    public String getSexoParticipante() {
        return sexoParticipante;
    }

    public void setSexoParticipante(String sexoParticipante) {
        this.sexoParticipante = sexoParticipante;
    }

    public String getEmailParticipante() {
        return emailParticipante;
    }

    public void setEmailParticipante(String emailParticipante) {
        this.emailParticipante = emailParticipante;
    }

    public String getTipoParticipante() {
        return tipoParticipante;
    }

    public void setTipoParticipante(String tipoParticipante) {
        this.tipoParticipante = tipoParticipante;
    }

    public String getAlteracaoParticipante() {
        return alteracaoParticipante;
    }

    public void setAlteracaoParticipante(String alteracaoParticipante) {
        this.alteracaoParticipante = alteracaoParticipante;
    }
    
    
}
